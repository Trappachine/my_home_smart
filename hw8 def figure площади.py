def triangle():
    height = int(input("Введите высоту: "))
    basis = int(input("Введите основу: "))
    print("Площадь треугольника ", 0.5 * basis * height)
def circle():
    d = int(input("Введите диаметр: "))
    print("Длина окружности ", 3.14 * d)
def square():
    long = int(input("Введите длину: "))
    print("Периметр квадрата", long * 4)
